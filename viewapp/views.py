from django.http import HttpResponse


def page(title, text, items):
    html_items = ""

    for item in items:
        html_items += f"<li>{item}</li>"

    html = f"""
    <!DOCTYPE html>
    <html lang="uk">
    <head>
        <meta charset="UTF-8">
        <title>{title}</title>
    </head>
    <body>
        <nav>
            <a href="/">Головна</a> |
            <a href="/news">Новини міста</a> |
            <a href="/management">Голови міста</a> |
            <a href="/facts">Факти про місто</a> |
            <a href="/contacts">Контактні телефони</a>
        </nav>

        <h1>{title}</h1>
        <p>{text}</p>

        <ul>
            {html_items}
        </ul>
    </body>
    </html>
    """

    return HttpResponse(html)


